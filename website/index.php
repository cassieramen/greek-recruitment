	<h2>Introduction</h2>
	<p>Sorority Recruitment is a five day process consisting of four ‘sets.’ Each set constricts the number 
		of sororities a potential new member, person going through recruitment, visits. For example, set one 
		consists of twelve parties. Each potential new members visits all twelve possible sororities for 
		thirty minutes each. Set two, each potential new member visits nine possible sororities for fifty 
		minutes each. And on and on until only three sororities remain. The last night, Set four, each 
		sorority orders the n girls who came to their house from 1 to n, with 30 girls receiving a 1 and the 
		rest numbers 2 to n. The stable marriage algorithm then pairs each potential new member with a sorority.</p>
	<h2>Objective</h2>
	<p>We would like to accurately predict what round of recruitment girls will be cut (1-4), or ultimately 
		accepted (5) by Alpha Phi sorority based on a variety of information collected about them.
		The Greek system is a huge part of social life with 40% of students at Northwestern participating in the 
		recruitment process, with over 600 people considered for a sorority. Studies have shown links between 
		the recruitment process and stress, anxiety and depression. If there were found to be some accurate 
		predictor or correlation with a variable it could potentially serve as a valuable tool for students 
		thinking about participating in Greek life and save them large amounts of stress and anxiety. Similarly if 
		there seemed to be any evidence of discrimination in the recruiting process it could prompt review and 
		discussion of the recruitment process and the role of Greek life as a whole at Universities across the 
		country. We have access to recruitment data usually kept private within sororities and are afforded a unique 
		opportunity to uncover information about this integral part of university social life. If our work is 
		successful it could easily be ported to not only the 11 other sororities on campus, but to fraternities and 
		sororities nationwide.</p>
